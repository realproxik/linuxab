static void fbcon_draw_char(struct fbcon *con, uint32_t x, uint32_t y, char c)
{
    uint32_t fg = con->fg_color;
    uint32_t bg = con->bg_color;
    uint8_t idx = (uint8_t)c;

    if (idx < 32 || idx > 126) {
        /* Non-printable: draw block */
        for (uint32_t row = 0; row < con->char_h; row++) {
            for (uint32_t col = 0; col < con->char_w; col++) {
                uint32_t *p = (uint32_t *)((uint8_t *)con->fb_base +
                                           (y + row) * con->pitch +
                                           (x + col) * 4);
                *p = bg;
            }
        }
        return;
    }

    const uint8_t *glyph = font_8x16[idx - 32];
    for (uint32_t row = 0; row < con->char_h; row++) {
        uint8_t rowbits = glyph[row];
        for (uint32_t col = 0; col < con->char_w; col++) {
            uint32_t color = (rowbits & (0x80 >> col)) ? fg : bg;
            uint32_t *p = (uint32_t *)((uint8_t *)con->fb_base +
                                       (y + row) * con->pitch +
                                       (x + col) * 4);
            *p = color;
        }
    }
}

void fbcon_init(struct fbcon *con, uint32_t *base, uint32_t width, uint32_t height,
                uint32_t pitch, uint32_t bpp)
{
    con->fb_base = base;
    con->width = width;
    con->height = height;
    con->pitch = pitch;
    con->bpp = bpp;
    con->fg_color = 0xFFFFFF; /* White */
    con->bg_color = 0x000000; /* Black */
    con->char_w = 8;
    con->char_h = 16;
    con->cols = width / 8;
    con->rows = height / 16;
    con->cursor_x = 0;
    con->cursor_y = 0;
}

void fbcon_clear(struct fbcon *con)
{
    for (uint32_t y = 0; y < con->height; y++) {
        for (uint32_t x = 0; x < con->width; x++) {
            uint32_t *p = (uint32_t *)((uint8_t *)con->fb_base + y * con->pitch + x * 4);
            *p = con->bg_color;
        }
    }
    con->cursor_x = 0;
    con->cursor_y = 0;
}

void fbcon_scroll(struct fbcon *con)
{
    /* Move all lines up by char_h */
    uint8_t *dst = (uint8_t *)con->fb_base;
    uint8_t *src = dst + con->char_h * con->pitch;
    uint32_t move_size = (con->height - con->char_h) * con->pitch;
    memcpy(dst, src, move_size);

    /* Clear last line */
    uint8_t *last = dst + move_size;
    for (uint32_t i = 0; i < con->char_h * con->pitch; i += 4) {
        *(uint32_t *)(last + i) = con->bg_color;
    }

    con->cursor_y = con->rows - 1;
    con->cursor_x = 0;
}

void fbcon_putc(struct fbcon *con, char c)
{
    if (c == '\n') {
        con->cursor_x = 0;
        con->cursor_y++;
    } else if (c == '\r') {
        con->cursor_x = 0;
    } else if (c == '\b') {
        if (con->cursor_x > 0) con->cursor_x--;
    } else if (c == '\t') {
        con->cursor_x = (con->cursor_x + 8) & ~7;
    } else {
        fbcon_draw_char(con, con->cursor_x * con->char_w,
                        con->cursor_y * con->char_h, c);
        con->cursor_x++;
    }

    if (con->cursor_x >= con->cols) {
        con->cursor_x = 0;
        con->cursor_y++;
    }

    if (con->cursor_y >= con->rows) {
        fbcon_scroll(con);
    }
}

void fbcon_puts(struct fbcon *con, const char *s)
{
    while (*s)
        fbcon_putc(con, *s++);
}

void fbcon_set_color(struct fbcon *con, uint32_t fg, uint32_t bg)
{
    con->fg_color = fg;
    con->bg_color = bg;
}

void fbcon_draw_cursor(struct fbcon *con, bool visible)
{
    uint32_t x = con->cursor_x * con->char_w;
    uint32_t y = con->cursor_y * con->char_h;
    uint32_t color = visible ? con->fg_color : con->bg_color;

    for (uint32_t row = con->char_h - 2; row < con->char_h; row++) {
        for (uint32_t col = 0; col < con->char_w; col++) {
            uint32_t *p = (uint32_t *)((uint8_t *)con->fb_base +
                                       (y + row) * con->pitch +
                                       (x + col) * 4);
            *p = color;
        }
    }
}
